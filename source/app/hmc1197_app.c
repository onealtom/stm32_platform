/*************************************************************
Name:InitHmc1197        
Description: 系统初始化
Input:void
Output:void      
Return:void
**************************************************************/
void InitHmc1197(void)
{
	UINT16 i;

	Hmc1197CsEnalbe(1);
	
	if(NET_TYPE_DCS1800==fpga_cfg.a_net_type )	
	{
	
		if(G2_TYPE_UNICOM==G2Tpye  )
		{
			for(i=0;i<(sizeof(HMC1197_RegVal_unicom_dcs)/(4*2));i++)
			{

				WriteHmc1197((UCHAR8)HMC1197_RegVal_unicom_dcs[i][0], (UINT32)HMC1197_RegVal_unicom_dcs[i][1]);
			
			}
		}else if (G2_TYPE_CNMOBILE==G2Tpye)
		{
			if((version_number == VERSION_40M_IN_A)||(version_number == VERSION_50M_IN_V4))
			{
				for(i=0;i<(sizeof(HMC1197_RegVal_cnmobile_dcs_v3a)/(4*2));i++)
				{
					WriteHmc1197((UCHAR8)HMC1197_RegVal_cnmobile_dcs_v3a[i][0], (UINT32)HMC1197_RegVal_cnmobile_dcs_v3a[i][1]);
				}
			}
			else
			{
				for(i=0;i<(sizeof(HMC1197_RegVal_cnmobile_dcs)/(4*2));i++)
				{
					WriteHmc1197((UCHAR8)HMC1197_RegVal_cnmobile_dcs[i][0], (UINT32)HMC1197_RegVal_cnmobile_dcs[i][1]);
				}
			}
		}
	}
	else if (NET_TYPE_GSM900==fpga_cfg.a_net_type)
	{
	
		if((version_number == VERSION_40M_NOIN) || (version_number == VERSION_40M_IN_A)
		|| (version_number == VERSION_40M_IN_B)
		)
		{
		
			for(i=0;i<(sizeof(HMC1197_RegVal_cnmobile_gsm_40M)/(4*2));i++)
			{

				WriteHmc1197((UCHAR8)HMC1197_RegVal_cnmobile_gsm_40M[i][0], (UINT32)HMC1197_RegVal_cnmobile_gsm_40M[i][1]);
		
			}
		}
		else if((version_number == VERSION_20M_NOIN) || (version_number == VERSION_20M_IN)
		|| (version_number == VERSION_40M_IN_C) || (version_number == VERSION_40M_IN_E)
		)
		{
		
			for(i=0;i<(sizeof(HMC1197_RegVal_cnmobile_gsm_20M)/(4*2));i++)
			{

				WriteHmc1197((UCHAR8)HMC1197_RegVal_cnmobile_gsm_20M[i][0], (UINT32)HMC1197_RegVal_cnmobile_gsm_20M[i][1]);
		
			}
		}
		else if((version_number == VERSION_50M_IN_F)||(version_number == VERSION_50M_IN_V4))
		{
		
			for(i=0;i<(sizeof(HMC1197_RegVal_cnmobile_gsm_50M)/(4*2));i++)
			{

				WriteHmc1197((UCHAR8)HMC1197_RegVal_cnmobile_gsm_50M[i][0], (UINT32)HMC1197_RegVal_cnmobile_gsm_50M[i][1]);
		
			}
		}
	}
	else if (NET_TYPE_CDMA800==fpga_cfg.a_net_type)
	{
		
		for(i=0;i<(sizeof(HMC1197_RegVal_cnmobile_gsm_40M)/(4*2));i++)
		{

			WriteHmc1197((UCHAR8)HMC1197_RegVal_telecom_cmda800[i][0], (UINT32)HMC1197_RegVal_telecom_cmda800[i][1]);
		
		}
	}

	
}














